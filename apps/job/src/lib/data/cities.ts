export const getCitiesRecommendation = (query: string): string[] => {
    if (!query) return [];
    const lowerQuery = query.toLowerCase();
    return PAKISTAN_CITIES.filter(city =>
        city.toLowerCase().includes(lowerQuery)
    ).slice(0, 10); // Return top 10 matches
};
